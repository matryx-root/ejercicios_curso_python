# Pedimos al usuario que ingrese la temperatura en grados Celsius
temperatura_celsius = float(input("Ingrese la temperatura en grados Celsius: "))

# Convertimos la temperatura de Celsius a Fahrenheit
temperatura_fahrenheit = 1.8 * temperatura_celsius + 32

# Imprimimos la temperatura en grados Fahrenheit
print("La temperatura es de", temperatura_fahrenheit, "grados Fahrenheit.")
